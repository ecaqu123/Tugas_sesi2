package projectelsa;

public class No2 {
    public static void main(String[] args) {

        int a = 100;
        int b = 20;
        int c = 528;
        int d = 70;
        int e = 256;
        int f = 3;
        int g = 128;
        int h = 5;
        int i = 600;
        int j = 4;
        int k = 423;
        int l = 2;
        int hasil;
      
    hasil = a & b;
    System.out.println("Hasil dari 100 & 20 : " + hasil );  
      
    hasil = c | d;
    System.out.println("Hasil dari 528 | 70 : " + hasil );  
      
    hasil = e >> f;
    System.out.println("Hasil dari 256 >> 3 : " + hasil );  
      
    hasil = g << h;
    System.out.println("Hasil dari 128 << 5 : " + hasil );  
      
    hasil = i >> j;
    System.out.println("Hasil dari 600 >> 4 : " + hasil );  
      
    hasil = k << l;
    System.out.println("Hasil dari 423 << 2 : " + hasil );  
    }
}